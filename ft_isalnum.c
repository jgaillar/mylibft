#include "libft.h"

int		ft_isalnum(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (ft_isalpha(c));
	else if (c >= '0' && c <= '9')
		return (ft_isdigit(c));
	else
		return (0);
}
