#include "libft.h"

int		ft_isalpha(int c)
{
	if (c >= 'A' && c <= 'Z')
		return (ft_isupper(c));
	else if (c >= 'a' && c <= 'z')
		return (ft_islower(c));
	else
		return (0);
}
